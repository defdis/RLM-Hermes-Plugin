#!/usr/bin/env bash
set -euo pipefail

# в”Ђв”Ђв”Ђ RLM Plugin for Hermes Agent вЂ” Auto Installer в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
# This script installs the RLM (Recursive Language Model) plugin for Hermes.
# Works on any Linux/macOS with Python 3.12+ and an OpenAI-compatible API.
# в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

echo ""
echo -e "${CYAN}в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—${NC}"
echo -e "${CYAN}в•‘     RLM Plugin Installer for Hermes Agent                в•‘${NC}"
echo -e "${CYAN}в•‘     Recursive Language Model вЂ” deep analysis             в•‘${NC}"
echo -e "${CYAN}в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ${NC}"
echo ""

# в”Ђв”Ђв”Ђ Check Python в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
PYTHON=""
for py in python3.12 python3.11 python3.13 python3; do
    if command -v "$py" &>/dev/null; then
        ver=$("$py" -c "import sys; print(f'{sys.version_info.major}.{sys.version_info.minor}')")
        major=$(echo "$ver" | cut -d. -f1)
        minor=$(echo "$ver" | cut -d. -f2)
        if [ "$major" -ge 3 ] && [ "$minor" -ge 11 ]; then
            PYTHON="$py"
            break
        fi
    fi
done

if [ -z "$PYTHON" ]; then
    echo -e "${RED}ERROR: Python 3.11+ required. Install it first:${NC}"
    echo "  Ubuntu/Debian: sudo apt install python3.12 python3.12-venv"
    echo "  macOS:         brew install python@3.12"
    exit 1
fi
echo -e "${GREEN}вњ“${NC} Python: $PYTHON ($($PYTHON --version))"

# в”Ђв”Ђв”Ђ Check Hermes в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
HERMES_HOME="${HERMES_HOME:-$HOME/.hermes}"
if [ ! -d "$HERMES_HOME" ]; then
    echo -e "${RED}ERROR: Hermes not found at $HERMES_HOME${NC}"
    echo "  Install Hermes first: https://hermes-agent.nousresearch.com"
    exit 1
fi
echo -e "${GREEN}вњ“${NC} Hermes found at $HERMES_HOME"

# в”Ђв”Ђв”Ђ API Credentials в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${YELLOW}API Configuration${NC}"
echo "RLM needs an OpenAI-compatible API endpoint."
echo ""
echo "Examples:"
echo "  OpenAI:      https://api.openai.com/v1"
echo "  OpenRouter:  https://openrouter.ai/api/v1"
echo "  Ollama:      http://localhost:11434/v1"
echo "  vLLM:        http://your-server:8000/v1"
echo ""

read -r -p "API Base URL: " BASE_URL
read -r -p "API Key: " API_KEY
read -r -p "Model name [gpt-4o]: " MODEL
MODEL="${MODEL:-gpt-4o}"

# в”Ђв”Ђв”Ђ Install RLM library в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${CYAN}Installing RLM library...${NC}"

RLM_DIR="$HERMES_HOME/rlm"
RLM_REPO="$RLM_DIR/repo"
RLM_VENV="$RLM_DIR/.venv"
PINNED_COMMIT="156fd72"

mkdir -p "$RLM_DIR"

if [ ! -d "$RLM_REPO" ]; then
    git clone https://github.com/alexzhang13/rlm.git "$RLM_REPO"
    cd "$RLM_REPO" && git checkout "$PINNED_COMMIT" && cd - > /dev/null
fi

$PYTHON -m venv "$RLM_VENV"
"$RLM_VENV/bin/pip" install -q -e "$RLM_REPO"

echo -e "${GREEN}вњ“${NC} RLM library installed"

# в”Ђв”Ђв”Ђ Configure environment в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${CYAN}Configuring environment...${NC}"

ENV_FILE="$HERMES_HOME/.env"

# Remove old RLM lines if present
if [ -f "$ENV_FILE" ]; then
    sed -i '/^RLM_OPENAI_/d' "$ENV_FILE"
fi

cat >> "$ENV_FILE" <<EOF
RLM_OPENAI_BASE_URL=$BASE_URL
RLM_OPENAI_API_KEY=$API_KEY
RLM_MODEL=$MODEL
EOF

echo -e "${GREEN}вњ“${NC} Environment configured"

# в”Ђв”Ђв”Ђ Install plugin в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${CYAN}Installing Hermes plugin...${NC}"

PLUGIN_DIR="$HERMES_HOME/plugins/rlm"
mkdir -p "$PLUGIN_DIR"

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
if [ -f "$SCRIPT_DIR/__init__.py" ]; then
    cp "$SCRIPT_DIR/__init__.py" "$PLUGIN_DIR/__init__.py"
else
    echo -e "${RED}ERROR: __init__.py not found. Are you running from the repo root?${NC}"
    exit 1
fi

# Copy plugin.yaml manifest
if [ -f "$SCRIPT_DIR/plugin.yaml" ]; then
    cp "$SCRIPT_DIR/plugin.yaml" "$PLUGIN_DIR/plugin.yaml"
    echo -e "${GREEN}вњ“${NC} Plugin manifest installed"
fi

# Copy SKILL.md so the agent knows when and how to use rlm_complete
if [ -f "$SCRIPT_DIR/skills/rlm-deep-analysis/SKILL.md" ]; then
    cp "$SCRIPT_DIR/skills/rlm-deep-analysis/SKILL.md" "$PLUGIN_DIR/SKILL.md"
    echo -e "${GREEN}вњ“${NC} Skill installed (agent instructions)"
fi

echo -e "${GREEN}вњ“${NC} Plugin installed"

# в”Ђв”Ђв”Ђ Restart Hermes в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${CYAN}Restarting Hermes...${NC}"
echo -e "${YELLOW}вљ ${NC} Please restart Hermes manually to load the new plugin."
echo "   If using systemd: sudo systemctl restart hermes-gateway-*.service"
echo "   Or use your Hermes management tool (hermes-ctl, /restart command, etc.)"

# в”Ђв”Ђв”Ђ Done в”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђв”Ђ
echo ""
echo -e "${GREEN}в•”в•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•—${NC}"
echo -e "${GREEN}в•‘     RLM Plugin installed successfully!                  в•‘${NC}"
echo -e "${GREEN}в•љв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ђв•ќ${NC}"
echo ""
echo "Verify: ask your Hermes agent 'Compare RAG and RLM in 3 bullet points'"
